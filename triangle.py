'''Функция для вычисления площади треугольника, принимает стороны треугольника и возвращает площадь'''
def area(a, b, c):
    return (a + b + c) / 2

'''Функция для вычисления периметра треугольника, принимает стороны треугольника и возвращает периметр'''
def perimeter(a, b, c):
    return a + b + c
